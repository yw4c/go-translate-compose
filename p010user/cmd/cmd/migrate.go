package cmd

import (
	"log"
	"translate/P10User/src/lib/db"
	"translate/P10User/src/migration"

	"github.com/spf13/cobra"
)

// migrateCmd represents the migrate command
var migrateCmd = &cobra.Command{
	Use:   "migrate",
	Short: "database migration",

	Run: func(cmd *cobra.Command, args []string) {

		m:= migration.New(&db.LocalConn{})
		switch args[0] {
		case "up":
			m.Up()
			break
		case "down":
			m.Rollback()
			break
		default:
			log.Println("Invalid arg, migrate args : \n up \n down")
		}
	},
}

